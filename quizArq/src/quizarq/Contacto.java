
package quizarq;

class Contacto {
    private String nombre;
    private int numero;
    public Contacto(String nombre, int numero) {
        this.nombre = nombre;
        this.numero = numero;
        
    }
       public String getNombre() {

return this.nombre;
    }
       public int getNumero() {

return this.numero;
    }
        
}